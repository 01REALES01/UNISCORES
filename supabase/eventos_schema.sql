-- ACTUALIZACIÓN DE SCHEMA PARA EVENTOS EN VIVO
-- ============================================

-- Tabla de Jugadores/Participantes
create table if not exists jugadores (
  id bigint generated by default as identity primary key,
  nombre text not null,
  numero int,
  equipo text not null, -- 'equipo_a' o 'equipo_b'
  partido_id bigint references partidos(id) on delete cascade,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Tabla de Eventos del Partido
create table if not exists eventos_partido (
  id bigint generated by default as identity primary key,
  partido_id bigint references partidos(id) on delete cascade not null,
  tipo_evento text not null, -- 'gol', 'tarjeta_amarilla', 'tarjeta_roja', 'inicio', 'fin'
  minuto int not null,
  jugador_id bigint references jugadores(id),
  equipo text not null, -- 'equipo_a' o 'equipo_b'
  descripcion text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

