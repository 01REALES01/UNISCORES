-- Índices para mejor performance
create index if not exists idx_jugadores_partido on jugadores(partido_id);
create index if not exists idx_eventos_partido on eventos_partido(partido_id);

-- RLS Policies
alter table jugadores enable row level security;
alter table eventos_partido enable row level security;

create policy "Jugadores are viewable by everyone" 
  on jugadores for select using (true);

create policy "Eventos are viewable by everyone" 
  on eventos_partido for select using (true);

create policy "Enable insert for authenticated users" 
  on jugadores for insert with check (true);

create policy "Enable insert for authenticated users eventos" 
  on eventos_partido for insert with check (true);

create policy "Enable update for authenticated users" 
  on jugadores for update using (true);

create policy "Enable delete for authenticated users" 
  on jugadores for delete using (true);
