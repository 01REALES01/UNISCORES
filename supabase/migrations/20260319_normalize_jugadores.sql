-- PHASE 7: Create helper view for backward compatibility (if needed)
-- ═════════════════════════════════════════════════════════════════════════════════

DROP VIEW IF EXISTS public.vw_olympics_jugadores CASCADE;

CREATE VIEW public.vw_olympics_jugadores AS
SELECT
    j.id,
    j.nombre,
    j.numero,
    j.profile_id,
    j.carrera_id,
    rp.partido_id,
    rp.equipo_a_or_b as equipo,
    j.created_at
FROM public.jugadores j
LEFT JOIN public.roster_partido rp ON rp.jugador_id = j.id;

COMMENT ON VIEW public.vw_olympics_jugadores IS
'Backward compatibility view. Reconstructs the old olympics_jugadores structure from normalized tables.
Use this if frontend code still expects partido_id in jugadores rows.';

-- Grant permissions on view
GRANT SELECT ON public.vw_olympics_jugadores TO PUBLIC;
