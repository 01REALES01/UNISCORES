-- ═══════════════════════════════════════════════════════════════════════════════
-- TABLE: puntos_config
-- Points awarded per finishing position, split by sport type.
-- tipo_deporte: 'equipo' (team), 'individual', 'barras' (crowd/cheering)
-- posicion: 1 = 1st place, 8 = last
-- ═══════════════════════════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.puntos_config (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tipo_deporte    TEXT NOT NULL,
    posicion        INT NOT NULL CHECK (posicion BETWEEN 1 AND 8),
    puntos          INT NOT NULL CHECK (puntos >= 0),
    descripcion     TEXT,
    created_at      TIMESTAMPTZ DEFAULT NOW() NOT NULL,
    updated_at      TIMESTAMPTZ DEFAULT NOW() NOT NULL,

    UNIQUE(tipo_deporte, posicion)
);

COMMENT ON TABLE public.puntos_config IS
'Configurable Olympic points per finishing position per sport type.
Edited by admins; referenced by clasificacion_disciplina to compute totals.';

