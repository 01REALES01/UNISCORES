-- ═══════════════════════════════════════════════════════════════════════════════
-- TABLE: clasificacion_disciplina
-- Admin records which carrera finished at which position in each discipline.
-- puntos_obtenidos is snapshotted from puntos_config at time of entry
-- (stored so config changes don't retroactively alter historical results).
-- ═══════════════════════════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.clasificacion_disciplina (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    disciplina_id   BIGINT NOT NULL REFERENCES public.disciplinas(id) ON DELETE CASCADE,
    carrera_id      BIGINT NOT NULL REFERENCES public.carreras(id) ON DELETE CASCADE,
    genero          TEXT NOT NULL DEFAULT 'masculino'
                    CHECK (genero IN ('masculino', 'femenino', 'mixto')),
    posicion        INT NOT NULL CHECK (posicion BETWEEN 1 AND 8),
    puntos_obtenidos INT NOT NULL DEFAULT 0,
    notas           TEXT,
    created_at      TIMESTAMPTZ DEFAULT NOW() NOT NULL,
    updated_at      TIMESTAMPTZ DEFAULT NOW() NOT NULL,
    created_by      UUID REFERENCES public.profiles(id) ON DELETE SET NULL,

    UNIQUE(disciplina_id, carrera_id, genero)
);

COMMENT ON TABLE public.clasificacion_disciplina IS
'Final Olympic positions per discipline per carrera.
Points are snapshotted at entry time — config changes do not affect past entries.
view_clasificacion_general aggregates these for the public leaderboard.';

CREATE INDEX IF NOT EXISTS idx_clasificacion_disciplina_id
    ON public.clasificacion_disciplina(disciplina_id);
CREATE INDEX IF NOT EXISTS idx_clasificacion_carrera_id
    ON public.clasificacion_disciplina(carrera_id);

-- RLS
ALTER TABLE public.clasificacion_disciplina ENABLE ROW LEVEL SECURITY;

CREATE POLICY "clasificacion_select_all"
    ON public.clasificacion_disciplina FOR SELECT USING (true);

CREATE POLICY "clasificacion_insert_staff"
    ON public.clasificacion_disciplina FOR INSERT
    WITH CHECK (
        public.has_role(auth.uid(), 'admin')
        OR public.has_role(auth.uid(), 'data_entry')
    );

CREATE POLICY "clasificacion_update_staff"
    ON public.clasificacion_disciplina FOR UPDATE
    USING (
        public.has_role(auth.uid(), 'admin')
        OR public.has_role(auth.uid(), 'data_entry')
    );

CREATE POLICY "clasificacion_delete_admins"
    ON public.clasificacion_disciplina FOR DELETE
    USING (public.has_role(auth.uid(), 'admin'));


-- ═══════════════════════════════════════════════════════════════════════════════
-- VIEW: view_clasificacion_general
-- Total points per carrera across all disciplines.
-- Returns per-discipline breakdown as JSONB for UI accordion.
-- ═══════════════════════════════════════════════════════════════════════════════

DROP VIEW IF EXISTS public.view_clasificacion_general;

CREATE VIEW public.view_clasificacion_general AS
SELECT
    c.id                                            AS carrera_id,
    c.nombre                                        AS carrera_nombre,
    c.escudo_url,
    COALESCE(SUM(cd.puntos_obtenidos), 0)           AS total_puntos,
    COUNT(cd.id)                                    AS disciplinas_participadas,
    JSONB_AGG(
        JSONB_BUILD_OBJECT(
            'disciplina_id',      d.id,
            'disciplina_nombre',  d.name,
            'genero',             cd.genero,
            'posicion',           cd.posicion,
            'puntos',             cd.puntos_obtenidos
        ) ORDER BY cd.puntos_obtenidos DESC
    ) FILTER (WHERE cd.id IS NOT NULL)              AS detalle_disciplinas
FROM public.carreras c
LEFT JOIN public.clasificacion_disciplina cd ON cd.carrera_id = c.id
LEFT JOIN public.disciplinas d ON d.id = cd.disciplina_id
GROUP BY c.id, c.nombre, c.escudo_url
ORDER BY total_puntos DESC, carrera_nombre ASC;

GRANT SELECT ON public.view_clasificacion_general TO authenticated, anon;

