ALTER TABLE public.carrera_disciplina ENABLE ROW LEVEL SECURITY;

-- Everyone can see enrollment (drives public career profiles)
CREATE POLICY "cd_select_all"
    ON public.carrera_disciplina FOR SELECT USING (true);

-- Only admin / data_entry can modify enrollment
CREATE POLICY "cd_write_staff"
    ON public.carrera_disciplina FOR ALL
    USING (
        public.has_role(auth.uid(), 'admin')
        OR public.has_role(auth.uid(), 'data_entry')
    )
    WITH CHECK (
        public.has_role(auth.uid(), 'admin')
        OR public.has_role(auth.uid(), 'data_entry')
    );

