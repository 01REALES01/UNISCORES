-- Tipos de Usuario (Enum)
create type user_role as enum ('admin', 'data_entry', 'public');

-- Tabla de Perfiles (Extiende la tabla auth.users de Supabase)
create table profiles (
  id uuid references auth.users on delete cascade not null primary key,
  email text,
  role user_role default 'public',
  full_name text,
  assigned_discipline_id bigint references disciplinas(id),
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Tabla de Disciplinas
create table disciplinas (
  id bigint generated by default as identity primary key,
  name text not null,
  icon text -- nombre del icono (lucide)
);

